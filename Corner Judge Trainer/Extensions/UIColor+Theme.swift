import UIKit

extension UIColor {
    
    static func flatRedColor() -> UIColor {
        return UIColor(red: 186.0 / 255.0, green: 66.0 / 255.0, blue: 66.0 / 255.0, alpha: 1.0)
    }
    
    static func flatBlueColor() -> UIColor {
        return UIColor(red: 54.0 / 255.0, green: 108.0 / 255.0, blue: 172.0 / 255.0, alpha: 1.0)
    }
    
    static func flatBlackColor() -> UIColor {
        return UIColor(white: 39.0 / 255.0, alpha: 1.0)
    }
    
    static func flatWhiteColor() -> UIColor {
        return UIColor(white: 245.0 / 255.0, alpha: 1.0)
    }
}
